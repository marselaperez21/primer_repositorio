
package clases;


public class calculadora {
    public float valor1 ;
    public float valor2 ;
    public float resultado;
    
    public void calcular_suma() {
    this.resultado= this.valor1+ this.valor2;
    }
     public void calcular_resta() {
    this.resultado= this.valor1-this.valor2;
    }
      public void calcular_multiplicacion() {
    this.resultado= this.valor1*this.valor2;
    }
       public void calcular_divicion() {
    this.resultado= this.valor1/this.valor2;
    }

}
